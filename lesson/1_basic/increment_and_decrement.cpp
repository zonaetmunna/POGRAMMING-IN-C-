#include <iostream>

int main() {
    int count = 5;

    std::cout << "Initial count: " << count << std::endl;

    count++; // Increment
    std::cout << "After increment: " << count << std::endl;

    count--; // Decrement
    std::cout << "After decrement: " << count << std::endl;

    return 0;
}

